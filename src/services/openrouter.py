TOPIC_LABELS = {
    "love": "Любовь и отношения",
    "career": "Карьера и работа",
    "money": "Деньги и финансы",
    "psy": "Внутреннее состояние / психика",
}


async def get_spread_interpretation(
    card_names: list[str],
    topic: str,
    question: str | None,
    is_pro: bool,
) -> str:
    """
    Запрашивает интерпретацию расклада у AI через OpenRouter.

    :param card_names: список названий карт (3 шт.)
    :param topic: тема расклада (love/career/money/psy/question)
    :param question: текст пользовательского вопроса (если topic == 'question')
    :param is_pro: флаг PRO-подписки (влияет на лимит токенов)
    :return: текст интерпретации
    """
    if not settings.OPENROUTER_API_KEY:
        return "⚠️ OpenRouter API ключ не настроен. Обратитесь к администратору."

    max_tokens = 2500 if is_pro else 700
    system_prompt_to_use = PRO_SYSTEM_PROMPT if is_pro else SYSTEM_PROMPT

    topic_text = f"Тема расклада: {TOPIC_LABELS.get(topic, topic)}"
    
    if question:
        # Защита от Prompt Injection: убираем спецсимволы и ограничиваем тегами
        safe_question = question.replace("<", "").replace(">", "").strip()
        topic_text += f"\nКонкретный вопрос или ситуация пользователя (в тегах): <user_query>{safe_question}</user_query>"

    cards_text = "\n".join(f"- {name}" for name in card_names)
    user_message = (
        f"{topic_text}\n\n"
        f"Карты расклада:\n{cards_text}"
    )

    payload = {
        "model": settings.OPENROUTER_MODEL,
        "max_tokens": max_tokens,
        "messages": [
            {"role": "system", "content": system_prompt_to_use},
            {"role": "user", "content": user_message},
        ],
    }

    headers = {
        "Authorization": f"Bearer {settings.OPENROUTER_API_KEY}",
        "Content-Type": "application/json",
        "HTTP-Referer": "https://tarot-bot.tg",
        "X-Title": "Tarot Bot",
    }

    max_retries = 3
    for attempt in range(max_retries):
        try:
            session = await get_session()
            async with session.post(
                OPENROUTER_URL,
                json=payload,
                headers=headers,
                timeout=aiohttp.ClientTimeout(total=45),
            ) as response:
                if response.status != 200:
                    error_text = await response.text()
                    logger.error("OpenRouter error %s: %s", response.status, error_text)
                    if response.status >= 500 and attempt < max_retries - 1:
                        import asyncio
                        logger.info("Retrying OpenRouter... (Attempt %d)", attempt + 2)
                        await asyncio.sleep(2 ** attempt)
                        continue
                    return "⚠️ Не удалось получить интерпретацию. Попробуйте позже."

                data = await response.json()
                return data["choices"][0]["message"]["content"].strip()

        except aiohttp.ClientError as e:
            logger.error("OpenRouter request failed: %s", e)
            if attempt < max_retries - 1:
                import asyncio
                await asyncio.sleep(2 ** attempt)
                continue
            return "⚠️ Ошибка соединения с AI. Попробуйте позже."
        except Exception as e:
            # Ловим все остальные ошибки (в том числе asyncio.TimeoutError)
            logger.error("OpenRouter unexpected error: %s", e)
            if attempt < max_retries - 1:
                import asyncio
                await asyncio.sleep(2 ** attempt)
                continue
            return "⚠️ Долгий ответ от AI. Попробуйте позже."


async def get_weekly_report_interpretation(stats: dict) -> str:
    """
    Запрашивает генерацию недельного отчета у ИИ на основе статистики.
    """
    system_prompt = WEEKLY_REPORT_PROMPT
    
    top_cards_str = ", ".join(stats.get('top_cards', [])) if stats.get('top_cards') else "Нет данных"
    peak_day_str = stats.get('peak_day', 'Нет данных')
    
    user_prompt = (
        f"Вот моя статистика за последние 7 дней:\n"
        f"- Средний стресс-индекс: {stats.get('avg_stress', 0):.2f}/1.0\n"
        f"- Доминирующая сфера: {stats.get('dominant_area', 'Не определено')}\n"
        f"- Топ-3 повторяющиеся карты: {top_cards_str}\n"
        f"- Пиковый день запросов: {peak_day_str}\n\n"
        f"Пожалуйста, проанализируй это и составь отчет длиной около 300 слов."
    )

    payload = {
        "model": settings.OPENROUTER_MODEL,
        "max_tokens": 1000,
        "messages": [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt},
        ],
    }

    headers = {
        "Authorization": f"Bearer {settings.OPENROUTER_API_KEY}",
        "Content-Type": "application/json",
        "HTTP-Referer": "https://tarot-bot.tg",
        "X-Title": "Tarot Bot",
    }

    try:
        session = await get_session()
        async with session.post(
            OPENROUTER_URL,
            json=payload,
            headers=headers,
            timeout=aiohttp.ClientTimeout(total=60),
        ) as response:
            if response.status != 200:
                error_text = await response.text()
                logger.error("OpenRouter report error %s: %s", response.status, error_text)
                return "Извини, не удалось сформировать отчет. Попробуй позже."
            
            data = await response.json()
            return data["choices"][0]["message"]["content"].strip()

    except Exception as e:
        logger.error("OpenRouter request failed (Weekly Report): %s", e)
        return "Произошла ошибка при составлении отчета. Попробуй позже."
